package com.example.webservice_retrofit_volley.Model;

public class Posts {
    private  String text;
    private Integer number;
    private boolean found;
    private String type;

    public String getText() {
        return text;
    }

    public Integer getNumber() {
        return number;
    }

    public boolean isFound() {
        return found;
    }

    public String getType() {
        return type;
    }
}
